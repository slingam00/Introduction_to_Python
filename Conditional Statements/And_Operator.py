day = "Tuesday"

if day == "Sunday":
    print("I will go on a run today")      # first if-statement is checked and is false
elif day == "Tuesday":                     # then elif-statement is checked and is true
    print("I will eat tacos today")        # "I will eat tacos today" is printed
else:                                                                                             
    print("I will neither go on a run nor eat tacos today")
