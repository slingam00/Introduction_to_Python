# If Statements
if True:
  print("The If statement is True")
 
if 2 > 1:
  print("2 is greater than 1")
 

# Else Statements
if 2 < 1:
  print("2 is less than 1")
else:
  print("2 is not less than 1")

  
# Elif Statements
if 2 < 1:
  print("2 is less than 1")
elif 2 > 1:
  print("2 is greater than 1")
else:
  print("2 is equal to 1")
